void convert_move_to_drow_dcol(Move_2048 move, int &drow, int &dcol) {
    switch (move) {
        case Move_2048::UP:
            drow = -1;
            dcol = 0;
            return;
        case Move_2048::DOWN:
            drow = 1;
            dcol = 0;
            return;
        case Move_2048::LEFT:
            drow = 0;
            dcol = -1;
            return;
        case Move_2048::RIGHT:
            drow = 0;
            dcol = 1;
            return;
        default:
            //should never get here...
            assert (false);
            return;
    }
}

